	mpz_t N; mpz_init(N); // modulus
	mpz_t D; mpz_init(D); // private exponent
	mpz_t E; mpz_init(E); // public exponent
	
	for (u32 i = 0; i < sizeof(modulus); i++)
		modulus[i] = reverse(modulus[i]);

